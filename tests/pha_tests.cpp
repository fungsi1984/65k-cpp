// NOLINTNEXTLINE
TEST(PHATests, PushedOverflow)
{
    emulator::Cpu cpu;
    cpu.reg.a  = 0x42;
    cpu.reg.sp = 0x00;

    constexpr std::array<std::uint8_t, 2> program{0x48, 0x48};
    emulator::execute(cpu, {program.data(), program.size()});

    ASSERT_EQ(cpu.reg.sp, 0xfe);
    ASSERT_EQ(cpu.mem[0x0100], 0x42);
    ASSERT_EQ(cpu.mem[0x01ff], 0x42);
}

